package _b_chapter2._b_binary_arithmetic_operators;

public class ArithmeticOperators {

    /*
      addition (+),
      subtraction (-),
      multiplication (*),
      division (/),
      and modulus (%)*/
    public static void main(String[] args) {
        double d = 39.21;
        //float f = 2.1; // will not compile
        float f = 2.1f;

        double result = d+f;
        System.out.println(result);
        System.out.println(f);
    }
}
